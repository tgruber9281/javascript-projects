// create an array variable containing the names
let names = ['Trevor', 'Amanda', 'Mao', 'Doodle', 'Finn', 'Gideon']
// write a for loop that prints each name on a different line
for (let i = 0; i < names.length; i ++) {
    for (let namedex = 0; namedex < names[i].length; namedex ++) {
        console.log(names[i][namedex]);
    }
}